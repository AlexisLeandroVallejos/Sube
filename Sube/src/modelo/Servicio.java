package modelo;

public class Servicio{
	private String nombre;
	private double costo;
	
	public double getCosto() {
		return costo;
	}
	public void setCosto(double d) {
		this.costo = d;
		
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
}
